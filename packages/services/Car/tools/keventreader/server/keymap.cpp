Keymap& Keymap::get() {
    static Keymap gKeymap;

    return gKeymap;
}

Keymap::Keymap() {
    fillMap();
}

std::string_view Keymap::getDisplayName(int keycode) {
    auto iter = mKeyMap.find(keycode), end = mKeyMap.end();
    if (iter == end) {
        std::stringstream ss;
        ss << "unknown " << keycode;
        return ss.str();
    }
    return iter->second;
}

